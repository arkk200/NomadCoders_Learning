# 튜플이나 리스트를 만들 때 복수형을 쓴다.

websites = (
    "google.com",
    "airbnb.com",
    "twitter.com",
    "facebook.com",
    "youtube.com"
)

# 집합체라면 for in을 이용해서 개수 만큼 반복문을 돌릴 수 있다.
# for, in 사이에 변수를 통해서 값을 가져온다.
# 값을 가져오는 변수명은 튜플, 리스트명의 단수 형태로 쓴다.
# 그러나 단어는 내 마음대로 정할 수 있다.
for website in websites:
    print("potato is equals to", website)

for xxxx in websites:
    print("potato is equals to", xxxx)