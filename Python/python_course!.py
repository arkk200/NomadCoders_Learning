def plus(a = 1, b = 1):
    print(a, '+', b, '=', a + b)

def minus(a = 1, b = 1):
    print(a, '-', b, '=', a - b)

def multiplication(a = 1, b = 1):
    print(a, '*', b, '=', a * b)

def division(a = 1, b = 1):
    print(a, '/', b, '=', a / b)

def power(a = 1, b = 1):
    print(a, '^', b, '=', a ** b)

plus(7, 8)
minus(13, 4)
multiplication(7, 9)
division(7, 3)
power(4, 3)